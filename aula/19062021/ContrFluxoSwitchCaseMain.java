public class ContrFluxoSwitchCaseMain {
    public static void main(String[] args) {
        char letra = 'C';
        switch (letra){
            case 'A':
                System.out.println(letra);
                break;
            case 'C':
                System.out.println(letra);
                break;
            case 'B':
                System.out.println(letra);
                break;
            default:
                System.out.println('?');
        }
    }
}
